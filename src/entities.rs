use crate::components::*;
use specs::{Builder, World, WorldExt};

// Create a wall entity
pub fn create_wall(world: &mut World, position: Position) {
    world
        .create_entity()
        .with(Position { z: 10, ..position })
        .with(Renderable {
            path: "/resources/images/wall.png".to_string(),
        })
        .with(Wall {})
        .with(Immovable)
        .build();
}

